/**
 * Tracking Service — Business logic for tracking codes and history.
 * Handles code generation, approval workflow, validation, public lookup, and event management.
 *
 * Approval Flow:
 * 1. Customer linked to BL → auto-generate code (pending)
 * 2. Admin reviews pending codes → approve or reject
 * 3. On approve → code activated + email/WhatsApp prepared for customer
 * 4. On reject → code deactivated with reason
 */
