/**
 * Generate a tracking code for a BL+Customer.
 * Manual generation by admin — starts as "pending" for approval.
 */
export async function generateTrackingCode(
  blId: number,
  customerId: number,
  expiresInDays: number = 365,
  adminUserId?: number
): Promise<repo.TrackingCodeRecord> {
  // Validate BL exists
  const bl = await blRepo.findBlById(blId);
  if (!bl) throw new Error("BL não encontrado.");

  // Validate customer exists
  const customer = await customerRepo.findCustomerById(customerId);
  if (!customer) throw new Error("Cliente não encontrado.");

  // Check for existing pending/approved code
  const existing = await repo.findCodeByBlAndCustomer(blId, customerId);
  if (existing && (existing.approvalStatus === "pending" || existing.approvalStatus === "approved")) {
    throw new Error(
      `Já existe um código ${existing.approvalStatus === "pending" ? "pendente de aprovação" : "ativo"} para este cliente e BL.`
    );
  }

  const code = await repo.createTrackingCode(blId, customerId, {
    expiresInDays,
    autoGenerated: false,
    approvalStatus: "pending",
  });

  await logAudit({
    userId: adminUserId ?? null,
    action: "create",
    entity: "tracking_code",
    entityId: code.id,
    changes: {
      created: { before: null, after: code.code },
      blId: { before: null, after: blId },
      customerId: { before: null, after: customerId },
      approvalStatus: { before: null, after: "pending" },
    },
  });

  // Notify owner about pending code (fire-and-forget)
  notifyPendingApproval({
    blNumber: bl.blNumber,
    trackingCode: code.code,
    customerName: customer.fullName,
    vehicleDescription: bl.vehicleDescription,
    autoGenerated: false,
  }).catch((err) => console.error("[Tracking Service] Pending notification error:", err));

  return code;
}

/**
 * Auto-generate a tracking code when a customer is linked to a BL.
 * Called internally — creates code as "pending" for admin review.
 */
export async function autoGenerateTrackingCode(
  blId: number,
  customerId: number
): Promise<repo.TrackingCodeRecord | null> {
  // Validate BL exists
  const bl = await blRepo.findBlById(blId);
  if (!bl) return null;

  // Validate customer exists
  const customer = await customerRepo.findCustomerById(customerId);
  if (!customer) return null;

  // Check for existing code (avoid duplicates)
  const existing = await repo.findCodeByBlAndCustomer(blId, customerId);
  if (existing && existing.approvalStatus !== "rejected") {
    // Already has a pending or approved code — skip
    return existing;
  }

  const code = await repo.createTrackingCode(blId, customerId, {
    expiresInDays: 365,
    autoGenerated: true,
    approvalStatus: "pending",
  });

  secureLogger.info(
    `[Tracking Service] Auto-generated tracking code ${code.code} (pending) for BL ${bl.blNumber} / Customer ${customer.fullName}`
  );

  // Notify owner about pending approval (fire-and-forget)
  notifyPendingApproval({
    blNumber: bl.blNumber,
    trackingCode: code.code,
    customerName: customer.fullName,
    vehicleDescription: bl.vehicleDescription,
    autoGenerated: true,
  }).catch((err) => console.error("[Tracking Service] Auto-gen notification error:", err));

  return code;
}

/**
 * Approve a pending tracking code — activates it and triggers notifications.
 */
export async function approveCode(
  id: number,
  adminUserId: number
): Promise<repo.TrackingCodeRecord> {
  const code = await repo.findTrackingCodeById(id);
  if (!code) throw new Error("Código de tracking não encontrado.");
  if (code.approvalStatus !== "pending") {
    throw new Error(`Código já está ${code.approvalStatus === "approved" ? "aprovado" : "rejeitado"}.`);
  }

  const approved = await repo.approveTrackingCode(id, adminUserId);
  if (!approved) throw new Error("Erro ao aprovar código.");

  await logAudit({
    userId: adminUserId,
    action: "update",
    entity: "tracking_code",
    entityId: id,
    changes: {
      approvalStatus: { before: "pending", after: "approved" },
      isActive: { before: false, after: true },
    },
  });

  // Get BL and customer for notifications
  const bl = await blRepo.findBlById(code.blId);
  const customer = await customerRepo.findCustomerById(code.customerId);

  if (bl && customer) {
    // Notify owner + prepare email/WhatsApp for customer (fire-and-forget)
    notifyCodeApproved({
      blNumber: bl.blNumber,
      trackingCode: code.code,
      customerName: customer.fullName,
      customerEmail: customer.email,
      customerPhone: customer.phone,
      customerId: customer.id,
      vehicleDescription: bl.vehicleDescription,
    }).catch((err) => console.error("[Tracking Service] Approval notification error:", err));
  }

  return approved;
}

/**
 * Reject a pending tracking code with optional reason.
 */
export async function rejectCode(
  id: number,
  reason?: string,
  adminUserId?: number
): Promise<repo.TrackingCodeRecord> {
  const code = await repo.findTrackingCodeById(id);
  if (!code) throw new Error("Código de tracking não encontrado.");
  if (code.approvalStatus !== "pending") {
    throw new Error(`Código já está ${code.approvalStatus === "approved" ? "aprovado" : "rejeitado"}.`);
  }

  const rejected = await repo.rejectTrackingCode(id, reason);
  if (!rejected) throw new Error("Erro ao rejeitar código.");

  await logAudit({
    userId: adminUserId ?? null,
    action: "update",
    entity: "tracking_code",
    entityId: id,
    changes: {
      approvalStatus: { before: "pending", after: "rejected" },
      rejectionReason: { before: null, after: reason ?? null },
    },
  });

  return rejected;
}

